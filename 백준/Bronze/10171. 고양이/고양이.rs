//아래 예제와 같이 고양이를 출력하시오
//고양이를 출력한다.
//\    /\
// )  ( ')
//(  /  )
// \(__)|
fn main() {
    println!("{0}    {1}{0}", "\\", "/");
    println!(" {0}  {1} {2}{0}", ")", "(", "'");
    println!("{0}  {1}  {2}", "(", "/", ")");
    println!(" {0}{1}{2}{2}{3}{4}", "\\", "(", "_", ")", "|",);
}
